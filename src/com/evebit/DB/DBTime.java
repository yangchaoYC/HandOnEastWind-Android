package com.evebit.DB;

public class DBTime {
	   private int id;//主键ID
	   private String nid;//主键ID
	   private String time;//主键ID

	   public int getId() {
		   return id;
	   }
	   public void setId(int id) {
		   this.id = id;
	   }
	   
	   public String getNid() {
	   return nid;
	   }
	   public void setNid(String nid) {
	   this.nid = nid;
	   }
	   public String getTime() {
	   return time;
	   }
	   public void setTime(String time) {
	   this.time = time;
	   }
}
